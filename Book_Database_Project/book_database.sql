CREATE TABLE VENDOR(
    VENDOR_ID INT PRIMARY KEY,
    VENDOR_NAME VARCHAR(30),
    VENDOR_PHONE CHAR(12),
    VENDOR_AREACODE CHAR(3),
    VENDOR_STATE VARCHAR(25)
);

CREATE TABLE PRODUCT(
    PROD_ID INT PRIMARY KEY,
    P_QOH INT,
    VENDOR_ID INT,
    FOREIGN KEY (VENDOR_ID) REFERENCES VENDOR(VENDOR_ID),
    P_PRICE DECIMAL(10, 2)
);

CREATE TABLE CUSTOMER(
    CUSTOMER_ID INT PRIMARY KEY,
    CUSTOMER_FNAME VARCHAR(30),
    CUSTOMER_LNAME VARCHAR(30),
    MEMBERSHIP_STATUS VARCHAR(30),
    STREET_NUMBER CHAR(10),
    STREET_NAME VARCHAR(30),
    CITY VARCHAR(30),
    ZIPCODE CHAR(10),
    STATE VARCHAR(25)
);

CREATE TABLE MEMBERSHIP(
    MEMBER_ID INT PRIMARY KEY ,
    MEMBER_YEARLY_COST DECIMAL(10, 2),
    MEMBER_DISCOUNT DECIMAL(10, 2),
    MEMBER_PHONENUM CHAR(12),
    MEMBER_EMAIL VARCHAR(50),
    CUSTOMER_ID INT,
    FOREIGN KEY (CUSTOMER_ID) REFERENCES CUSTOMER(CUSTOMER_ID)
);

CREATE TABLE SHIPPING_METH(
    SHIPPING_METH_ID INT PRIMARY KEY,
    METHOD_ID INT,
    SHIP_COST DECIMAL(10, 2)
);

CREATE TABLE CUSTOMER_ORDER(
    ORDER_ID INT PRIMARY KEY,
    CUSTOMER_ID INT,
    SHIPPING_METH_ID INT,
    FOREIGN KEY (CUSTOMER_ID) REFERENCES CUSTOMER(CUSTOMER_ID),
    FOREIGN KEY (SHIPPING_METH_ID) REFERENCES SHIPPING_METH(SHIPPING_METH_ID),
    ORDER_DATE DATE,
    ORDER_ADDRESS VARCHAR(50)
);

CREATE TABLE ORDER_HISTORY(
    HISTORY_ID INT PRIMARY KEY,
    ORDER_ID INT,
    FOREIGN KEY (ORDER_ID) REFERENCES CUSTOMER_ORDER(ORDER_ID)
);

CREATE TABLE INVOICE(
    INV_NUM INT PRIMARY KEY,
    CUSTOMER_ID INT,
    FOREIGN KEY (CUSTOMER_ID) REFERENCES CUSTOMER(CUSTOMER_ID),
    INV_DATE DATE
);

CREATE TABLE LINE(
    INV_NUM INT PRIMARY KEY,
    FOREIGN KEY  (INV_NUM) REFERENCES INVOICE(INV_NUM),
    LINE_NUM INT,
    PROD_ID INT,
    LINE_UNITS VARCHAR(20),
    LINE_PRICE DECIMAL(10, 2)
);

CREATE TABLE TECHNOLOGY(
    TECH_ID INT PRIMARY KEY ,
    PROD_ID INT,
    FOREIGN KEY (PROD_ID) REFERENCES PRODUCT(PROD_ID),
    TECH_NAME VARCHAR(30),
    SIZE VARCHAR(20),
    EDITION DECIMAL,
    YEAR_MADE CHAR(4),
    TECH_PRICE DECIMAL(10, 2)
);

CREATE TABLE TOY(
    TOY_ID INT PRIMARY KEY,
    PROD_ID INT,
    FOREIGN KEY (PROD_ID) REFERENCES PRODUCT(PROD_ID),
    TOY_NAME VARCHAR(30)
);

CREATE TABLE PUBLISHER(
    PUBLISHER_ID INT PRIMARY KEY,
    PUBLICATION_NAME VARCHAR(30)
);

CREATE TABLE NARRATOR(
    NARRATOR_ID INT PRIMARY KEY ,
    NARRATOR_FNAME VARCHAR(30),
    NARRATOR_LNAME VARCHAR(30)
);

CREATE TABLE AUTHOR(
    AUTHOR_ID INT PRIMARY KEY,
    AUTHOR_FNAME VARCHAR(30),
    AUTHOR_LNAME VARCHAR(30)
);

CREATE TABLE BOOK_CAT(
    BOOK_CAT_ID INT PRIMARY KEY,
    FICTION VARCHAR(30),
    NONFICTION VARCHAR(30),
    SPECIALTY VARCHAR(30)
);

CREATE TABLE BOOK_LANGUAGE(
    LANGUAGE_ID INT PRIMARY KEY,
    LANGUAGE_NAME VARCHAR(30)
);

CREATE TABLE BOOK(
    BOOK_ID INT PRIMARY KEY,
    ISBN INT(13),
    PROD_ID INT ,
    FOREIGN KEY (PROD_ID) REFERENCES PRODUCT(PROD_ID),
    LANGUAGE_ID INT,
    PUBLISHER_ID INT,
    BOOK_CAT_ID INT,
    FOREIGN KEY (LANGUAGE_ID) REFERENCES BOOK_LANGUAGE(LANGUAGE_ID),
    FOREIGN KEY (PUBLISHER_ID) REFERENCES PUBLISHER(PUBLISHER_ID),
    FOREIGN KEY (BOOK_CAT_ID) REFERENCES BOOK_CAT(BOOK_CAT_ID),
    TITLE VARCHAR(30),
    PUBLICATION_DATE DATE,
    BOOK_TYPE VARCHAR(30),
    PAGE_NUM INT
);

CREATE TABLE BOOK_AUTHOR(
    BOOK_ID INT PRIMARY KEY,
    AUTHOR_ID INT,
    FOREIGN KEY (BOOK_ID) REFERENCES BOOK(BOOK_ID),
    FOREIGN KEY (AUTHOR_ID) REFERENCES AUTHOR(AUTHOR_ID)
);

CREATE TABLE AUDIOBOOK_VERS(
    BOOK_ID INT PRIMARY KEY,
    FOREIGN KEY (BOOK_ID) REFERENCES BOOK(BOOK_ID),
    NARRATOR_ID INT,
    FOREIGN KEY (NARRATOR_ID) REFERENCES NARRATOR(NARRATOR_ID),
    TIME DECIMAL(10, 2),
    AUDIO_FORMAT VARCHAR(30)
);



